//! Kimchi Poseidon sponge data for the Lean formalization (`formal/Kimchi/Sponge`).
//!
//! Emits two artifacts from `mina_poseidon`:
//!
//! * `PoseidonConstantsFq.lean` / `PoseidonConstantsFp.lean` — the `fq_kimchi` / `fp_kimchi`
//!   parameters (55 × 3 round constants and the 3 × 3 MDS matrix, over the Vesta and Pallas
//!   base fields respectively), as canonical `Nat` literals. Committed to
//!   `formal/Kimchi/Sponge/`; regenerate here on a proof-systems bump.
//! * `poseidon_fq_vectors.json` / `poseidon_fp_vectors.json` — absorb/squeeze traces of
//!   `ArithmeticSponge` over the same parameters (deterministic seeded inputs), the test
//!   vectors for the Lean sponge automaton.
//! * `fq_sponge_vectors.json` — op traces of `DefaultFqSponge<VestaParameters>` (scalar and
//!   point absorption, raw squeezes, 128-bit and endo-expanded challenges).
//! * `group_map_vectors.json` — `t ↦ to_group(t)` vectors of the SvdW map.
//!
//! Usage: `sponge_dump <lean-out-dir> <fixtures-out-dir>`.

use ark_ff::UniformRand;
use mina_curves::pasta::Fq;
use mina_poseidon::{
    constants::PlonkSpongeConstantsKimchi as SC,
    pasta::{fq_kimchi, FULL_ROUNDS},
    poseidon::{ArithmeticSponge, Sponge},
};
use rand::SeedableRng;
use rand_chacha::ChaCha20Rng;
use serde_json::json;
use std::fmt::Write as _;

fn fe<F: std::fmt::Display>(x: &F) -> String {
    format!("{}", x)
}

fn lean_matrix<F: std::fmt::Display>(name: &str, doc: &[&str], rows: &[[F; 3]]) -> String {
    let mut s = String::new();
    writeln!(s, "/-- {}", doc[0]).unwrap();
    for line in &doc[1..] {
        writeln!(s, "{line}").unwrap();
    }
    writeln!(s, "-/").unwrap();
    writeln!(s, "def {name} : Array (Array Nat) := #[").unwrap();
    for (i, row) in rows.iter().enumerate() {
        let sep = if i + 1 == rows.len() { "]" } else { "," };
        writeln!(s, "  #[{},", fe(&row[0])).unwrap();
        writeln!(s, "    {},", fe(&row[1])).unwrap();
        writeln!(s, "    {}]{}", fe(&row[2]), sep).unwrap();
    }
    s
}

/// Emit one field's Poseidon artifacts: the constants module and the automaton trace
/// vectors.
fn emit_poseidon<F: ark_ff::PrimeField>(
    params: &'static mina_poseidon::poseidon::ArithmeticSpongeParams<F, FULL_ROUNDS>,
    field_doc: &str,
    ns: &str,
    const_path: &str,
    vec_name: &str,
    vec_path: &str,
    seed: u8,
) {
    let mut lean = String::new();
    writeln!(
        lean,
        "/-!\n\
         # Kimchi Poseidon parameters over the {field_doc}\n\
         \n\
         The `{vec_name}` sponge parameters of proof-systems `mina_poseidon` (width 3, rate 2,\n\
         55 full rounds, `x^7` S-box, full MDS, no initial ARK), as canonical `Nat` values.\n\
         \n\
         Generated by `tools/fixture-dump` (`sponge_dump`); do not edit by hand.\n\
         -/\n\
         \n\
         namespace Kimchi.Sponge.{ns}\n"
    )
    .unwrap();
    lean.push_str(&lean_matrix(
        "roundConstants",
        &[
            "The 55 × 3 round constants: `roundConstants[r][i]` is added to state element",
            "`i` at the end of round `r`.",
        ],
        &params.round_constants,
    ));
    lean.push('\n');
    lean.push_str(&lean_matrix(
        "mds",
        &["The 3 × 3 MDS matrix, row-major."],
        &[params.mds[0], params.mds[1], params.mds[2]],
    ));
    lean.push_str(&format!("\nend Kimchi.Sponge.{ns}\n"));
    std::fs::write(const_path, lean).unwrap();
    println!("wrote {const_path}");

    let rng = &mut ChaCha20Rng::from_seed([seed; 32]);
    let shapes: Vec<Vec<(&str, usize)>> = vec![
        vec![("squeeze", 1)],
        vec![("absorb", 1), ("squeeze", 1)],
        vec![("absorb", 2), ("squeeze", 1)],
        vec![("absorb", 3), ("squeeze", 2)],
        vec![("absorb", 5), ("squeeze", 4)],
        vec![("absorb", 1), ("squeeze", 1), ("absorb", 1), ("squeeze", 3)],
        vec![("squeeze", 3), ("absorb", 2), ("squeeze", 1)],
    ];
    let cases: Vec<_> = shapes
        .iter()
        .map(|shape| {
            let mut sponge = ArithmeticSponge::<F, SC, FULL_ROUNDS>::new(params);
            let ops: Vec<_> = shape
                .iter()
                .map(|(op, count)| match *op {
                    "absorb" => {
                        let xs: Vec<F> = (0..*count).map(|_| F::rand(rng)).collect();
                        sponge.absorb(&xs);
                        json!({ "op": "absorb", "values": xs.iter().map(fe).collect::<Vec<_>>() })
                    }
                    _ => {
                        let outs: Vec<F> = (0..*count).map(|_| sponge.squeeze()).collect();
                        json!({ "op": "squeeze", "expect": outs.iter().map(fe).collect::<Vec<_>>() })
                    }
                })
                .collect();
            json!({ "ops": ops })
        })
        .collect();
    let vectors = json!({ "field": vec_name, "cases": cases });
    std::fs::write(vec_path, serde_json::to_string_pretty(&vectors).unwrap()).unwrap();
    println!("wrote {vec_path} ({} cases)", shapes.len());
}

/// Emit `DefaultFqSponge<P>` op traces: scalar and point absorption, raw field squeezes,
/// 128-bit prechallenges, and endo-expanded effective challenges. Covers the limb buffer
/// across consecutive challenges, each absorb kind resetting it, and (via the scalar/base
/// modulus comparison) the curve's `absorb_fr` encoding branch.
fn emit_fq_sponge<P: ark_ec::short_weierstrass::SWCurveConfig>(
    params: &'static mina_poseidon::poseidon::ArithmeticSpongeParams<P::BaseField, FULL_ROUNDS>,
    endo_r: P::ScalarField,
    curve: &str,
    seed: u8,
    path: &str,
) where
    P::BaseField: ark_ff::PrimeField,
    <P::BaseField as ark_ff::PrimeField>::BigInt:
        Into<<P::ScalarField as ark_ff::PrimeField>::BigInt>,
{
    use ark_ec::{AffineRepr, CurveGroup};
    use mina_poseidon::{sponge::ScalarChallenge, FqSponge};

    type FqS<P> = mina_poseidon::sponge::DefaultFqSponge<P, SC, FULL_ROUNDS>;
    let rng = &mut ChaCha20Rng::from_seed([seed; 32]);
    let rand_pt = |rng: &mut ChaCha20Rng| {
        (<ark_ec::short_weierstrass::Affine<P> as AffineRepr>::Group::rand(rng)).into_affine()
    };
    let shapes: Vec<Vec<&str>> = vec![
        vec!["challenge"],
        vec!["absorb_fr", "challenge"],
        vec!["absorb_fr", "challenge_fq"],
        vec!["absorb_g", "squeeze_challenge"],
        vec!["absorb_g", "challenge", "challenge"],
        vec![
            "absorb_fr",
            "challenge",
            "absorb_g",
            "squeeze_challenge",
            "challenge_fq",
        ],
        vec!["challenge_fq", "absorb_fr", "squeeze_challenge"],
    ];
    let cases: Vec<_> = shapes
        .iter()
        .map(|shape| {
            let mut sp = FqS::<P>::new(params);
            let ops: Vec<_> = shape
                .iter()
                .map(|op| match *op {
                    "absorb_fr" => {
                        let x = P::ScalarField::rand(rng);
                        sp.absorb_fr(&[x]);
                        json!({ "op": "absorb_fr", "value": fe(&x) })
                    }
                    "absorb_g" => {
                        let p = rand_pt(rng);
                        sp.absorb_g(&[p]);
                        json!({ "op": "absorb_g", "point": [fe(&p.x), fe(&p.y)] })
                    }
                    "challenge_fq" => {
                        json!({ "op": "challenge_fq", "expect": fe(&sp.challenge_fq()) })
                    }
                    "challenge" => {
                        json!({ "op": "challenge", "expect": fe(&sp.challenge()) })
                    }
                    _ => {
                        let c = ScalarChallenge::new(sp.challenge()).to_field(&endo_r);
                        json!({ "op": "squeeze_challenge", "expect": fe(&c) })
                    }
                })
                .collect();
            json!({ "ops": ops })
        })
        .collect();
    let vectors = json!({ "curve": curve, "cases": cases });
    std::fs::write(path, serde_json::to_string_pretty(&vectors).unwrap()).unwrap();
    println!("wrote {path} ({} cases)", shapes.len());
}

fn main() {
    let mut args = std::env::args().skip(1);
    let lean_dir = args
        .next()
        .expect("usage: poseidon_dump <lean-out-dir> <fixtures-out-dir>");
    let fix_dir = args
        .next()
        .expect("usage: poseidon_dump <lean-out-dir> <fixtures-out-dir>");

    emit_poseidon::<Fq>(
        fq_kimchi::static_params(),
        "Vesta base field",
        "FqKimchi",
        &format!("{lean_dir}/PoseidonConstantsFq.lean"),
        "fq_kimchi",
        &format!("{fix_dir}/poseidon_fq_vectors.json"),
        44,
    );
    emit_poseidon::<mina_curves::pasta::Fp>(
        mina_poseidon::pasta::fp_kimchi::static_params(),
        "Pallas base field",
        "FpKimchi",
        &format!("{lean_dir}/PoseidonConstantsFp.lean"),
        "fp_kimchi",
        &format!("{fix_dir}/poseidon_fp_vectors.json"),
        47,
    );

    emit_fq_sponge::<mina_curves::pasta::VestaParameters>(
        fq_kimchi::static_params(),
        poly_commitment::ipa::endos::<mina_curves::pasta::Vesta>().1,
        "vesta",
        45,
        &format!("{fix_dir}/fq_sponge_vectors.json"),
    );
    emit_fq_sponge::<mina_curves::pasta::PallasParameters>(
        mina_poseidon::pasta::fp_kimchi::static_params(),
        poly_commitment::ipa::endos::<mina_curves::pasta::Pallas>().1,
        "pallas",
        48,
        &format!("{fix_dir}/fq_sponge_pallas_vectors.json"),
    );

    // --- group_map vectors ---
    // `t ↦ to_group(t)` at seeded field elements, from the production map.
    {
        use groupmap::GroupMap;
        let rng = &mut ChaCha20Rng::from_seed([46u8; 32]);
        let gm = groupmap::BWParameters::<mina_curves::pasta::VestaParameters>::setup();
        let vectors: Vec<_> = (0..8)
            .map(|_| {
                let t = Fq::rand(rng);
                let (x, y) = gm.to_group(t);
                json!({ "t": fe(&t), "x": fe(&x), "y": fe(&y) })
            })
            .collect();
        let out = json!({ "curve": "vesta", "vectors": vectors });
        let p = format!("{fix_dir}/group_map_vectors.json");
        std::fs::write(&p, serde_json::to_string_pretty(&out).unwrap()).unwrap();
        println!("wrote {p} (8 vectors)");
    }
}

fn pt(g: &mina_curves::pasta::Vesta) -> serde_json::Value {
    use ark_ec::AffineRepr;
    assert!(!g.is_zero(), "vector point unexpectedly at infinity");
    json!([fe(&g.x), fe(&g.y)])
}
